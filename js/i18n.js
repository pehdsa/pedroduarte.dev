let currentLang = localStorage.getItem("pedro-lang") || "en";

function setLanguage(lang) {
  currentLang = lang;
  localStorage.setItem("pedro-lang", lang);
  document.documentElement.lang = lang;

  // Update all elements with data-i18n
  document.querySelectorAll("[data-i18n]").forEach((el) => {
    const key = el.getAttribute("data-i18n");
    const text = translations[lang]?.[key] || translations.en[key];
    if (text) {
      // Preserve line breaks in text
      if (text.includes("\n")) {
        el.innerHTML = text.replace(/\n/g, "<br>");
      } else {
        el.textContent = text;
      }
    }
  });

  // Update meta tags
  const metaTitle = translations[lang]?.["meta.title"] || translations.en["meta.title"];
  const metaDesc = translations[lang]?.["meta.description"] || translations.en["meta.description"];
  document.title = metaTitle;
  document.querySelector('meta[name="description"]')?.setAttribute("content", metaDesc);
  document.querySelector('meta[property="og:title"]')?.setAttribute("content", metaTitle);
  document.querySelector('meta[property="og:description"]')?.setAttribute("content", metaDesc);

  // Update active lang in selector
  document.querySelectorAll("[data-lang]").forEach((btn) => {
    btn.classList.toggle("font-bold", btn.dataset.lang === lang);
    btn.classList.toggle("text-blue", btn.dataset.lang === lang);
  });

  // Update lang display text
  const langText = document.getElementById("lang-text");
  if (langText) langText.textContent = lang.toUpperCase();

  // Update WhatsApp link with localized message
  const waLink = document.getElementById("whatsapp-link");
  if (waLink) {
    const phone = waLink.dataset.phone || "5567992486520";
    const msg = translations[lang]?.["contact.whatsappMsg"] || translations.en["contact.whatsappMsg"];
    waLink.href = `https://wa.me/${phone}?text=${encodeURIComponent(msg)}`;
  }
}

function initI18n() {
  setLanguage(currentLang);
}

window.translations = translations;
